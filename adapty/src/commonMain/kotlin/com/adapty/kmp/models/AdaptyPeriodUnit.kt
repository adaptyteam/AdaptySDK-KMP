package com.adapty.kmp.models


/**
 * Units of time used for subscription periods.
 */
public enum class AdaptyPeriodUnit {
    /** One day period. */
    DAY,

    /** One week period. */
    WEEK,

    /** One month period. */
    MONTH,

    /** One year period. */
    YEAR,

    /** Unknown or unsupported period unit. */
    UNKNOWN
}

